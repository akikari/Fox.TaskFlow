//==================================================================================================
// Response DTO for task operations.
// Used in CQRS command and query responses.
//==================================================================================================

namespace Fox.TaskFlow.Application.DTOs.Responses;

//==================================================================================================
/// <summary>
/// Response model representing a task.
/// </summary>
//==================================================================================================
public sealed class TaskResponse
{
    //==============================================================================================
    /// <summary>
    /// Gets or sets the unique identifier of the task.
    /// </summary>
    //==============================================================================================
    public Guid Id { get; set; }

    //==============================================================================================
    /// <summary>
    /// Gets or sets the title of the task.
    /// </summary>
    //==============================================================================================
    public string Title { get; set; } = string.Empty;

    //==============================================================================================
    /// <summary>
    /// Gets or sets the description of the task.
    /// </summary>
    //==============================================================================================
    public string Description { get; set; } = string.Empty;

    //==============================================================================================
    /// <summary>
    /// Gets or sets the optional due date of the task.
    /// </summary>
    //==============================================================================================
    public DateTime? DueDate { get; set; }

    //==============================================================================================
    /// <summary>
    /// Gets or sets the current status of the task.
    /// </summary>
    //==============================================================================================
    public TaskStatus Status { get; set; }

    //==============================================================================================
    /// <summary>
    /// Gets or sets the date and time when the task was created.
    /// </summary>
    //==============================================================================================
    public DateTime CreatedAt { get; set; }

    //==============================================================================================
    /// <summary>
    /// Gets or sets the date and time when the task was last updated.
    /// </summary>
    //==============================================================================================
    public DateTime UpdatedAt { get; set; }
}
