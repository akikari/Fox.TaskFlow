//==================================================================================================
// Defines the workflow states a task can be in during its lifecycle.
// Used for workflow transitions and state validation.
//==================================================================================================

namespace Fox.TaskFlow.Domain.Enums;

//==================================================================================================
/// <summary>
/// Represents the status of a task in the workflow.
/// </summary>
//==================================================================================================
public enum TaskStatus
{
    //==============================================================================================
    /// <summary>
    /// Task has been created but not started.
    /// </summary>
    //==============================================================================================
    Created = 0,

    //==============================================================================================
    /// <summary>
    /// Task is currently being worked on.
    /// </summary>
    //==============================================================================================
    InProgress = 1,

    //==============================================================================================
    /// <summary>
    /// Task has been completed successfully.
    /// </summary>
    //==============================================================================================
    Completed = 2,

    //==============================================================================================
    /// <summary>
    /// Task has been cancelled and will not be completed.
    /// </summary>
    //==============================================================================================
    Cancelled = 3
}
