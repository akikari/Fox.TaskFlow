//==================================================================================================
// DTO for filtered task list response.
// Contains tasks matching filter criteria with total count.
//==================================================================================================

namespace Fox.TaskFlow.Blazor.Models;

//==================================================================================================
/// <summary>
/// Response containing filtered list of tasks.
/// </summary>
//==================================================================================================
public sealed class FilteredTasksDto
{
    //==============================================================================================
    /// <summary>
    /// Gets or sets the list of tasks matching the filter.
    /// </summary>
    //==============================================================================================
    public List<TaskDto> Tasks { get; set; } = [];

    //==============================================================================================
    /// <summary>
    /// Gets or sets the total count of tasks matching the filter.
    /// </summary>
    //==============================================================================================
    public int TotalCount { get; set; }
}
